	public static void handleObjects(final Player c, final int objectId, final int x, final int y, final int face) {
		c.clickObjectType = 0;
		c.turnPlayerTo(x, y);

		if (objectId == 28779) {
			for (int i = 0; i < CHAOS_OBJECTS.length; i++) {
				if(c.objectX == CHAOS_OBJECTS[i][0] && c.objectY == CHAOS_OBJECTS[i][1]) {
					c.getPA().movePlayer(CHAOS_OBJECTS[i][2], CHAOS_OBJECTS[i][3], 0);
				}
			}
		}
		for (int j = 0; j < CHAOS_OBJECTS2.length; j++) {
			if (objectId == CHAOS_OBJECTS2[j][0]) {
				if(c.objectX == CHAOS_OBJECTS2[j][1] && c.objectY == CHAOS_OBJECTS2[j][2]) {
					if(c.underAttackBy < 0 || c.underAttackBy2 < 0) {
						c.sendMessage("You cannot enter the rift whilst your under attack.");
					} else {
						c.getPA().movePlayer(CHAOS_OBJECTS2[j][3], CHAOS_OBJECTS2[j][4], 0);
					}
				}
			}
		}
		if (objectId == 28782) {
			for (int x1 = 0; x1 < CHAOS_OBJECTS3.length; x1++) {
				if(c.objectX == CHAOS_OBJECTS3[x1][0] && c.objectY == CHAOS_OBJECTS3[x1][1]) {
					c.getPA().movePlayer(CHAOS_OBJECTS3[x1][2], CHAOS_OBJECTS3[x1][3], 0);
				}
			}
		}
		if (objectId == 29537) {
			if(c.objectX == 3115 && c.objectY == 5528) {
				c.getPA().movePlayer(3142, 5545, 0);
			}
		}
		if (objectId == 28891) {
			if(c.underAttackBy < 0 || c.underAttackBy2 < 0) {
				c.sendMessage("You cannot enter the rift whilst your under attack.");
			} else {
				c.getPA().movePlayer(3183, 5470, 0);
			}
		}
	}
